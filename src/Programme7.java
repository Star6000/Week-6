/*Write a program to insert any temperature value in degree Fahrenheit and convert
        to degree Celsius ((F − 32) × 5/9 = 0°C).*/

public class Programme7 {
      public static void main(String[] args){
          int F = 32;
          int C = ((F - 32) * 5/9);
          System.out.println(C+"°C");
      }
}
